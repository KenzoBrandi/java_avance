package cardgame;

/**
 * Énumération représentant les différents types de jeux de cartes.
 * <p>
 * Cette énumération permet de définir si un jeu utilise un paquet de 32 ou 52 cartes.
 * </p>
 */
public enum GameType {
    /** Jeu utilisant un paquet de 32 cartes. */
    CARD32, 
    
    /** Jeu utilisant un paquet de 52 cartes. */
    CARD52;
}
